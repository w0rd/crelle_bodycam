local Tunnel = module("vrp", "lib/Tunnel")
local Proxy = module("vrp", "lib/Proxy")

vRP = Proxy.getInterface("vRP")
vRPclient = Tunnel.getInterface("vRP","crelle_bodycam")

vRP.defInventoryItem({"bodycam","Bodycam","Bodycam", function(args) 
    local choices = {}
    
    choices["> Tænd"] = {function(player,choice,mod)
        local user_id = vRP.getUserId({player})
        if user_id ~= nil then
            TriggerClientEvent("bodycam:show", player)
            vRP.closeMenu({player})
        	else
        end
    end}
  
    choices["> Sluk"] = {function(player,choice,mod)
      local user_id = vRP.getUserId({player})
      if user_id ~= nil then
          TriggerClientEvent("bodycam:close", player)
          vRP.closeMenu({player})
      	else
      end
  	end}
  
    return choices
end, 0.50})